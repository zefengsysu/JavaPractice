public class TreeNode {
    public int value;
    public TreeNode leftSubTree, rightSubTree;

    public TreeNode(int value, TreeNode leftSubTree, TreeNode rightSubTree) {
        this.value = value;
        this.leftSubTree = leftSubTree;
        this.rightSubTree = rightSubTree;
    }
}
